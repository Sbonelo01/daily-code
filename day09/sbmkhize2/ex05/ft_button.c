int		ft_button(int i, int j, int k)
{
	if ((i < j && j < k) || (k < j && j < i))
		return (j);
	else if ((j < i && i < k) || (k < i && i < k))
		return (i);
	else if ((i < k && k < j) || (j < k && k < i))
		return (k);
	else
		return (i);
}
